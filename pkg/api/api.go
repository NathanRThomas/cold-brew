package api 

import (
	"coldbrew/tools"
	"coldbrew/db/postgres"
	"coldbrew/tools/logging"
	
)

  //-----------------------------------------------------------------------------------------------------------------------//
 //----- CONSTS ----------------------------------------------------------------------------------------------------------//
//-----------------------------------------------------------------------------------------------------------------------//

  //-----------------------------------------------------------------------------------------------------------------------//
 //----- STRUCTS ---------------------------------------------------------------------------------------------------------//
//-----------------------------------------------------------------------------------------------------------------------//


type API struct {
	logging.Logger
	apiUrl tools.String 
	production bool

	db *postgres.Coldbrew
}


  //-----------------------------------------------------------------------------------------------------------------------//
 //----- FUNCTIONS -------------------------------------------------------------------------------------------------------//
//-----------------------------------------------------------------------------------------------------------------------//

func NewAPI (d *postgres.Coldbrew, apiUrl tools.String, productionFlag bool) *API {
	return &API {
		db: d,
		apiUrl: apiUrl,
		production: productionFlag,
	}
}

